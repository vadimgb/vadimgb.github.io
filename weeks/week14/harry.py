from logic import *

дождь = Symbol("дождь") # Идет дождь.
хагрид = Symbol("хагрид")# Гарри посетил Хагрида.
дамблдор = Symbol("дамблдор")# Гарри посетил Дамблдора.

знания = And(
        Implication(Not(дождь), хагрид),
        Or(хагрид, дамблдор),
        Not(And(хагрид, дамблдор)),
        дамблдор)

print(model_check(знания, дождь))

